#include "libft.h"

void	ft_bzero_bwd(void *s, size_t n)
{
	while (n)
	{
		if (n >= 8)
		{
			s -= (n -= 8) ? 8 : 8;
			*(long *)s &= 0x00;
		}
		else if (n >= 4)
		{
			s -= (n -= 4) ? 4 : 4;
			*(int *)s &= 0x00;
		}
		else
		{
			s -= (n -= 1) ? 1 : 1;
			*(char *)s &= 0x00;
		}
	}
}
